const ll mod = 1000000007;

struct mint {
  ll x; // typedef long long ll;
  mint(ll x=0):x((x%mod+mod)%mod){}
  mint operator-() const { return mint(-x);}
  mint& operator+=(const mint a) {
    if ((x += a.x) >= mod) x -= mod;
    return *this;
  }
  mint& operator-=(const mint a) {
    if ((x += mod-a.x) >= mod) x -= mod;
    return *this;
  }
  mint& operator*=(const mint a) {
    (x *= a.x) %= mod;
    return *this;
  }
  mint operator+(const mint a) const {
    mint res(*this);
    return res+=a;
  }
  mint operator-(const mint a) const {
    mint res(*this);
    return res-=a;
  }
  mint operator*(const mint a) const {
    mint res(*this);
    return res*=a;
  }
  mint pow(ll t) const {
    if (!t) return 1;
    mint a = pow(t>>1);
    a *= a;
    if (t&1) a *= *this;
    return a;
  }

  // for prime mod
  mint inv() const {
    return pow(mod-2);
  }
  mint& operator/=(const mint a) {
    return (*this) *= a.inv();
  }
  mint operator/(const mint a) const {
    mint res(*this);
    return res/=a;
  }
};

struct combination {
  std::vector<mint> fact, ifact;
  combination(int n):fact(n+1),ifact(n+1) {
    assert(n < mod);
    fact[0] = 1;
    for (int i = 1; i <= n; ++i) fact[i] = fact[i-1]*i;
    ifact[n] = fact[n].inv();
    for (int i = n; i >= 1; --i) ifact[i-1] = ifact[i]*i;
  }
  mint operator()(int n, int k) {
    if (k < 0 || k > n) return 0;
    return fact[n]*ifact[k]*ifact[n-k];
  }
};

mint dp[200010];

void dijkstra( ll s, std::vector<std::vector<P>> &G, std::vector<ll> &dist )
{
  std::priority_queue<P, std::vector<P>, std::greater<P>> pque;

  dist.assign( N+10, INF );
  dist[s] = 0;

  dp[s] = 1;

  pque.emplace( dist[s], s );

  while( !pque.empty() )
  {
    P p = pque.top(); pque.pop();
    ll d = p.first, v = p.second;

    if( dist[v] < d )
      continue;

    for( auto u : G[v] )
    {
      if( chmin( dist[u.first], dist[v]+u.second ) ) {
        pque.emplace( dist[u.first], u.first );
        dp[u.first] = dp[v];
      } else if( dist[u.first] == dist[v]+u.second ) {
        dp[u.first] += dp[v];
      }
    }
  }

  return;
}

